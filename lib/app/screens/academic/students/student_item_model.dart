class StudentItem {
  const StudentItem({
    required this.id,
    required this.name,
    required this.className,
    required this.section,
    required this.rollNo,
    required this.attendance,
    required this.email,
    required this.status,
  });

  final String id;
  final String name;
  final String className;
  final String section;
  final String rollNo;
  final String attendance;
  final String email;
  final String status;
}
